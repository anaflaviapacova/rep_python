def calculadora():
    num1 = float(input("Digite o primeiro número: "))
    num2 = float(input("Digite o segundo número: "))

    # Realiza as operações
    print(f"A soma de {num1} e {num2} é {num1 + num2}.")
    print(f"A subtração de {num1} por {num2} é {num1 - num2}.")
    print(f"A multiplicação de {num1} e {num2} é {num1 * num2}.")

    # Verifica se o segundo número é zero para a divisão
    if num2 != 0:
        print(f"A divisão de {num1} por {num2} é {num1 / num2:.2f}.")
    else:
        print("Não é possível dividir por zero.")

# Chamada da função
calculadora()